public class math2 {
    public static void main(String[] args) {
        double x = -5;
        double y = 2;

        System.out.println("Valor absoluto de " + x  + Math.abs(x));
        System.out.println("Potencia de " + y + " elevado a 3 é " + Math.pow(x, 3));
        System.out.println("a raiz quadrada de 16 é " + Math.sqrt(16));
        System.out.println("Maior valor entre 10 e 20: " + Math.max(10, 20));
        System.out.println("Seno de 90 graus: " + Math.sin(Math.PI/2));
        System.out.println("Logaritimo de 10 na base 2: " + Math.log10(10));
        System.out.println("numero aleatorio " + Math.random());

    }
}
